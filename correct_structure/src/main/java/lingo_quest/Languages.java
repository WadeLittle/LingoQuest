package lingo_quest;

/**
 * Enumerates the different languages supported by the application for language learning activities.
 * Each enum constant represents a specific language that the application can provide lessons and questions in.
 */
public enum Languages {
    SPANISH,   // Represents the Spanish language.
    FRENCH,    // Represents the French language.
    GERMAN,    // Represents the German language.
    JAPANESE,  // Represents the Japanese language.
    KOREAN,    // Represents the Korean language.
    DEFAULT    // Represents a default setting when no specific language is selected or applicable.
}
